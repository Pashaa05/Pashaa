
void main() {
  int a = 15;
  int b = 10;

  print('a == b: ${a == b}'); // Sama dengan
  print('a != b: ${a != b}'); // Tidak sama dengan
  print('a > b: ${a > b}');   // Lebih besar
  print('a < b: ${a < b}');   // Lebih kecil
  print('a >= b: ${a >= b}'); // Lebih besar atau sama dengan
  print('a <= b: ${a <= b}'); // Lebih kecil atau sama dengan
}